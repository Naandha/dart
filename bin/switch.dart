void main() {
  int year = 2025;
  switch (year) {
    case 2018:
      print("year is 2018");
      break;
    case 2019:
      print("year is 2019 ");
      break;
    case 2020:
      print("year is 2020");
      break;
    case 2021:
      print("year is 2021");
      break;
    case 2022:
      print("year is 2023");
      break;
    case 2023:
      print("year is 2023");
      break;
    default:
      print("year is invalid");
  }
}